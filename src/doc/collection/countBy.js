const _ = require("lodash");

/*
 * 接收 2 个参数：'数组'，'统计规则'；
 * 根据规则统计；
 * 返回一个对象，属性名为'数组'元素经过'统计规则'的运算结果，属性值为对应的元素数量；
 * --------------------------------------------------
 */
/* { '1': 3, '2': 3 } */
console.log(_.countBy([1.1, 1.2, 1.3, 2.1, 2.2, 2.3], Math.floor));
/* { '3': 2, '5': 1 } */
console.log(_.countBy(["one", "two", "three"], _.property("length")));
