const _ = require("lodash");

/*
 * 接收 2 个参数：'数组（或者对象）'，'映射规则'；
 * --------------------------------------------------
 */
console.log("----------: 基本用法");
/* [ 1, 4, 9 ] */
console.log(_.map([1, 2, 3], item => item * item));

/*
 * 第 1 个参数可以是'对象'；
 * 迭代对象的属性；
 * --------------------------------------------------
 */
/* [ 'name_tom', 'age_18' ] */
console.log(
  _.map(
    {
      name: "tom",
      age: 18
    },
    (value, key) => {
      return `${key}_${value}`;
    }
  )
);
